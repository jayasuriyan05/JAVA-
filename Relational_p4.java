package basics;
import java.util.Scanner;
public class Relational_p4 {
    public static void main(String[] args) {
		int n1 = 10;
		int n2 = 20;
		System.out.println(n1>n2);
		System.out.println(n1<n2);
		System.out.println(n1<=n2);
		System.out.println(n1>=n2);
		System.out.println(n1==n2);
		System.out.println(n1=n2);
	}
}
