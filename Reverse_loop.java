package basics;
import java.util.Scanner;
public class Reverse_loop {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the number :");
		int n1 = sc.nextInt();
		for (int i=n1; i>0; i--)
		{
			System.out.println("Jaya Suriyan VP" +i);
		}
	}

}
